    public void visualizarCompanias(){       
        ArrayList<Compania> companias = new ArrayList<Compania>();
        companias = Compania.leerCompanias();
        modelo.setRowCount(companias.size());
        int fila = 0;
        for(Compania compania: companias){
            tblCompanias.getModel().setValueAt(compania.getPrefijo(), fila, 0);
            tblCompanias.getModel().setValueAt(compania.getCodigo(), fila, 1);
            tblCompanias.getModel().setValueAt(compania.getNombre(), fila, 2);
            tblCompanias.getModel().setValueAt(compania.getDireccion(), fila, 3);
            tblCompanias.getModel().setValueAt(compania.getMunicipio(), fila, 4);
            tblCompanias.getModel().setValueAt(compania.getTelPasajero(), fila, 5);
            tblCompanias.getModel().setValueAt(compania.getTelAeropuerto(), fila, 6);
            fila++;
        }
    }
    

