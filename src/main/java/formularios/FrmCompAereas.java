    private void btnEliminarCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCompActionPerformed
        int indiceTabla = tblCompanias.getSelectedRow();
        if(indiceTabla == -1){
            lblError.setText("Debe seleccionar una compañía");
        }else{
            Object prefijoObjeto = tblCompanias.getValueAt(indiceTabla, 0);
            int prefijo = Integer.parseInt(prefijoObjeto.toString());
            String codigo = tblCompanias.getValueAt(indiceTabla, 1).toString();
            String nombre = tblCompanias.getValueAt(indiceTabla, 2).toString();
            String direccion = tblCompanias.getValueAt(indiceTabla, 3).toString();
            String municipio = tblCompanias.getValueAt(indiceTabla, 4).toString();
            String telPasajero = tblCompanias.getValueAt(indiceTabla, 5).toString();
            String telAeropuerto = tblCompanias.getValueAt(indiceTabla, 6).toString();
            Compania compania = new Compania(prefijo, codigo, nombre, direccion, municipio, telPasajero, telAeropuerto);
            Compania.eliminarCompania(compania);
            visualizarCompanias();
        }
    }//GEN-LAST:event_btnEliminarCompActionPerformed

